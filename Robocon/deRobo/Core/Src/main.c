void PWM_setFreq_Hz(TIM_HandleTypeDef *htim, uint32_t freq)
{
	uint32_t Ftim;
	if(htim->Instance == TIM1)
	{
		Ftim = HAL_RCC_GetPCLK2Freq();
		if(((RCC->CFGR >> 11) & 0x7) >= 0x4)
		{
			Ftim = Ftim * 2;
		}
	}else if(htim->Instance == TIM2 || htim->Instance == TIM3|| htim->Instance == TIM4)
	{
		Ftim = HAL_RCC_GetPCLK1Freq();
		if(((RCC->CFGR >> 8) & 0x7) >= 0x4)
		{
			Ftim = Ftim * 2;
		}
	}else 
	{
		Error_Handler();
	}
	uint16_t PSCtim = htim->Instance->PSC;
	uint16_t ARRtim = Ftim/((PSCtim + 1) * freq);
	__HAL_TIM_SetAutoreload(htim, ARRtim);
}
void PWM_setDuty(TIM_HandleTypeDef *htim, HAL_TIM_ActiveChannel Channel, uint8_t Duty)
{
	
	__HAL_TIM_SetCompare(htim,Channel, (__HAL_TIM_GetAutoreload(htim)*Duty/100));
}

